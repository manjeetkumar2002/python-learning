#There are two membership operator
# 1. in  2. not in

# in return true if that thing is present otherwise it returns false
# not in return true if that thing is not present otherwise it returns false

str = 'jenny'

print('j' in str) # true because j is present

print('j' not in str) # false because j is present

print('jenn'  in str) # true

list = [19,45,7,77,99,-12]

print(19 in list) #true